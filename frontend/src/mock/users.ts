// Helper to get next available extension number
export function getNextExtensionNumber(users: User[]): string {
  const usedNumbers = users
    .map(u => u.extension?.extension_number)
    .filter((n): n is string => !!n)
    .map(n => parseInt(n, 10));

  if (usedNumbers.length === 0) return '1001';

  const maxNumber = Math.max(...usedNumbers);
  return (maxNumber + 1).toString();
}

